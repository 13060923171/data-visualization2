    time_number = [960,986,1041,1069,1126,1129,1141,1164,1267,1279]
    tl = Timeline()
    for i in range(1,11):
        map = (
            Map(init_opts=opts.InitOpts(theme=ThemeType.INFOGRAPHIC))
            .add(
                "",
                 list_sum[i-1],
                 "china",
                 label_opts=opts.LabelOpts(is_show=False),
                 is_map_symbol_show=False,
                 itemstyle_opts={
                     "normal": {"areaColor": "#323c48", "borderColor": "#404a59"},
                     "emphasis": {
                         "label": {"show": Timeline},
                         "areaColor": "rgba(255,255,255, 0.5)",
                     },
                 },
                 )
            .set_global_opts(
                title_opts=opts.TitleOpts(
                    title="宋代重大事件-{}-{}".format(time_list[i-1],time_number[i-1]),
                    pos_left="center",
                    pos_top="top",
                    title_textstyle_opts=opts.TextStyleOpts(
                        font_size=25, color="rgba(47, 7, 19, 0.9)"
                    ),),
                tooltip_opts=opts.TooltipOpts(
                    is_show=True,
                    formatter=JsCode(
                        """function(params) {
                        if ('value' in params.data) {
                            
                        }
                    }"""
                    ),
                ),
                visualmap_opts=opts.VisualMapOpts(
                    is_calculable=True,
                    dimension=0,
                    pos_left="10",
                    pos_top="center",
                    range_text=["High", "Low"],
                    range_color=["lightskyblue", "yellow", "orangered"],
                    textstyle_opts=opts.TextStyleOpts(color="#ddd"),
                    min_=0,
                    max_=50,
                    is_show=False
                ),
            )
        )
        tl.add(map, "{}".format(time_list[int(i-1)]))
        tl.add_schema(
            # 播放速度
            play_interval=3000,
            # 是否显示timeline组件
            is_timeline_show=True,
            # 是否自动播放
            is_auto_play=True,
        )
    return tl


